import { createContext, useContext, useEffect, ReactNode, useState } from 'react'
import { useAuthStore } from '@/stores/authStore'
import { User, Session } from '@supabase/supabase-js'
import type { Database } from '@/types/supabase'

type Profile = Database['public']['Tables']['profiles']['Row']

interface AuthContextType {
  user: User | null
  session: Session | null
  profile: Profile | null
  isLoading: boolean
  isInitializing: boolean
  isAuthenticated: boolean
  isInitialized: boolean
  error: string | null
  signIn: (email: string, password: string) => Promise<void>
  signUp: (email: string, password: string, metadata?: { full_name?: string }) => Promise<void>
  signOut: () => Promise<void>
  resetPassword: (email: string) => Promise<void>
  updatePassword: (newPassword: string) => Promise<void>
  refreshSession: () => Promise<void>
  validateAndRecoverSession: () => Promise<boolean>
  updateProfile: (updates: Partial<Profile>) => Promise<void>
  clearError: () => void
}

const AuthContext = createContext<AuthContextType | undefined>(undefined)

interface AuthProviderProps {
  children: ReactNode
}

export function AuthProvider({ children }: AuthProviderProps) {
  const authStore = useAuthStore()
  const { isInitialized, isLoading, isAuthenticated, user, session, profile, error, isInitializing } = authStore

  // SSR Hydration Î∂àÏùºÏπò Î∞©ÏßÄÎ•º ÏúÑÌïú ÌÅ¥ÎùºÏù¥Ïñ∏Ìä∏ ÏÉÅÌÉú Í¥ÄÎ¶¨
  const [isClient, setIsClient] = useState(false)

  // ÌÅ¥ÎùºÏù¥Ïñ∏Ìä∏ ÏÇ¨Ïù¥ÎìúÏóêÏÑúÎßå Ïã§Ìñâ
  useEffect(() => {
    setIsClient(true)
  }, [])

  // ÎîîÎ≤ÑÍπÖÏö© ÏÉÅÌÉú Î°úÍ∑∏ (ÌÅ¥ÎùºÏù¥Ïñ∏Ìä∏ÏóêÏÑúÎßå)
  useEffect(() => {
    if (!isClient) return

    const sessionInfo = session ? {
      expiresAt: session.expires_at,
      timeToExpiry: session.expires_at ? Math.floor((session.expires_at * 1000 - Date.now()) / 1000 / 60) + ' minutes' : 'unknown',
      accessTokenLength: session.access_token?.length || 0
    } : null

    console.log('üèóÔ∏è AuthProvider state update:', {
      timestamp: new Date().toISOString(),
      isInitialized,
      isInitializing,
      isLoading,
      isAuthenticated,
      hasUser: !!user,
      hasSession: !!session,
      hasProfile: !!profile,
      hasError: !!error,
      userEmail: user?.email,
      profileRole: profile?.role,
      sessionInfo,
      errorMessage: error
    })
  }, [
    isClient,
    isInitialized,
    isInitializing,
    isLoading,
    isAuthenticated,
    user,
    session,
    profile,
    error
  ])

  useEffect(() => {
    // ÌÅ¥ÎùºÏù¥Ïñ∏Ìä∏ÏóêÏÑúÎßå Ïù∏Ï¶ù ÏÉÅÌÉú Ï¥àÍ∏∞Ìôî
    if (!isClient) return

    // Ïù∏Ï¶ù ÏÉÅÌÉú Ï¥àÍ∏∞Ìôî - Ìïú Î≤àÎßå Ïã§ÌñâÎêòÎèÑÎ°ù Î≥¥Ïû•
    if (!isInitialized && !isInitializing) {
      console.log('üîÑ AuthContext: Triggering auth initialization...')

      // Promise Ï≤¥Ïù∏ÏúºÎ°ú Ï¥àÍ∏∞Ìôî ÏÉÅÌÉú Î≥¥Ïû• (AuthStoreÏóêÏÑú ÏûêÏ≤¥Ï†ÅÏúºÎ°ú ÏóêÎü¨ Ï≤òÎ¶¨Ìï®)
      authStore.initialize().catch((error) => {
        console.error('‚ùå AuthContext initialization failed:', error)
        // AuthStoreÏóêÏÑú Ïù¥ÎØ∏ ÏóêÎü¨ Ï≤òÎ¶¨ÌïòÎØÄÎ°ú Ï∂îÍ∞Ä Ï≤òÎ¶¨Îäî Î∂àÌïÑÏöî
      })
    }
  }, [isClient, isInitialized, isInitializing])

  // ÏÑ∏ÏÖò Í∞±Ïã† ÌÉÄÏù¥Î®∏ ÏÑ§Ï†ï (Î∏åÎùºÏö∞Ï†Ä Ï∞Ω Ïù¥Îèô Ïãú ÏÑ∏ÏÖò Ïú†ÏßÄ) - ÌÅ¥ÎùºÏù¥Ïñ∏Ìä∏ÏóêÏÑúÎßå, Ìïú Î≤àÎßå ÏÑ§Ï†ï
  useEffect(() => {
    if (!isClient || !authStore.isAuthenticated || !authStore.session) return

    // Ï§ëÎ≥µ ÌÉÄÏù¥Î®∏ ÏÑ§Ï†ï Î∞©ÏßÄ
    if (typeof window !== 'undefined' && window.__sessionRefreshTimer) {
      clearInterval(window.__sessionRefreshTimer)
      if (window.__sessionFocusHandler) {
        window.removeEventListener('focus', window.__sessionFocusHandler)
      }
    }

    let isRefreshing = false // Ï§ëÎ≥µ Í∞±Ïã† Î∞©ÏßÄ ÌîåÎûòÍ∑∏

    // 1ÏãúÍ∞ÑÎßàÎã§ ÏÑ∏ÏÖò Í∞±Ïã†
    const refreshInterval = setInterval(async () => {
      const currentState = useAuthStore.getState()
      if (currentState.isAuthenticated && !isRefreshing) {
        isRefreshing = true
        try {
          await authStore.refreshSession()
        } catch (error) {
          console.error('Scheduled session refresh failed:', error)
        } finally {
          isRefreshing = false
        }
      }
    }, 60 * 60 * 1000) // 1ÏãúÍ∞Ñ

    // ÌéòÏù¥ÏßÄ Ìè¨Ïª§Ïä§ Ïãú ÏÑ∏ÏÖò Í≤ÄÏ¶ù Î∞è Î≥µÍµ¨ (ÌîÑÎ°úÏ†ùÌä∏ ÏÉÅÌÉú Î≥¥Ìò∏)
    const handleFocus = async () => {
      const currentState = useAuthStore.getState()

      console.log('üëÅÔ∏è Browser focus detected, validating session...')

      // Ïù∏Ï¶ùÎêú ÏÉÅÌÉúÍ∞Ä ÏïÑÎãàÎ©¥ Í≤ÄÏ¶ùÌï† ÌïÑÏöî ÏóÜÏùå
      if (!currentState.isAuthenticated || !currentState.session) {
        console.log('üìù No authenticated session to validate')
        return
      }

      // Ï§ëÎ≥µ Í≤ÄÏ¶ù Î∞©ÏßÄ
      if (isRefreshing) {
        console.log('üîÑ Session validation already in progress')
        return
      }

      // ÌòÑÏû¨ user ÏÉÅÌÉú Î∞±ÏóÖ (ÌîÑÎ°úÏ†ùÌä∏ Ïª®ÌÖçÏä§Ìä∏ Î≥¥Ìò∏Ïö©)
      const currentUser = currentState.user

      isRefreshing = true
      try {
        const validationResult = await authStore.validateAndRecoverSession()

        if (validationResult) {
          console.log('‚úÖ Session validation completed successfully')

          // ÏÑ∏ÏÖò Í≤ÄÏ¶ù ÌõÑ user ÏÉÅÌÉúÍ∞Ä ÏùºÏãúÏ†ÅÏúºÎ°ú Î≥ÄÍ≤ΩÎêú Í≤ΩÏö∞ ÌôïÏù∏
          const finalState = useAuthStore.getState()
          if (finalState.user?.id === currentUser?.id) {
            console.log('‚úÖ User identity maintained during validation')
          } else if (finalState.isAuthenticated && finalState.user) {
            console.log('‚ö†Ô∏è User identity changed during validation - this may affect project context')
          }
        } else {
          console.log('‚ùå Session validation failed - user logged out')
        }
      } catch (error) {
        console.error('‚ùå Focus session validation error:', error)
      } finally {
        isRefreshing = false
      }
    }

    if (typeof window !== 'undefined') {
      window.addEventListener('focus', handleFocus)
      // Ï†ÑÏó≠ Ï∞∏Ï°∞Î°ú Ï§ëÎ≥µ ÏÑ§Ï†ï Î∞©ÏßÄ
      window.__sessionRefreshTimer = refreshInterval
      window.__sessionFocusHandler = handleFocus
    }

    return () => {
      clearInterval(refreshInterval)
      if (typeof window !== 'undefined') {
        window.removeEventListener('focus', handleFocus)
        window.__sessionRefreshTimer = null
        window.__sessionFocusHandler = null
      }
    }
  }, [isClient, authStore.isAuthenticated]) // session ÏùòÏ°¥ÏÑ± Ï†úÍ±∞Î°ú Ï§ëÎ≥µ Ïã§Ìñâ Î∞©ÏßÄ

  // Î∏åÎùºÏö∞Ï†Ä Ï¢ÖÎ£å Ïãú ÏÑ∏ÏÖò Ï†ïÎ¶¨ - ÌÅ¥ÎùºÏù¥Ïñ∏Ìä∏ÏóêÏÑúÎßå
  useEffect(() => {
    if (!isClient || typeof window === 'undefined') return

    // Ïï± ÏãúÏûë Ïãú Ïù¥Ï†Ñ ÏÑ∏ÏÖò Ï†ïÎ¶¨ ÌîåÎûòÍ∑∏ Ï†úÍ±∞
    window.sessionStorage.removeItem('auth-keep-session')

    const handleBeforeUnload = () => {
      // Î∏åÎùºÏö∞Ï†Ä Ï¢ÖÎ£å/ÏÉàÎ°úÍ≥†Ïπ® Íµ¨Î∂Ñ Î∂àÍ∞ÄÎä•ÌïòÎØÄÎ°ú localStorage ÏÇ¨Ïö©
      localStorage.setItem('auth-unload-time', Date.now().toString())
    }

    const handleVisibilityChange = () => {
      if (document.visibilityState === 'hidden') {
        // ÌÉ≠Ïù¥ Ïà®Í≤®Ïßà Îïå (Î∏åÎùºÏö∞Ï†Ä Ï¢ÖÎ£å Í∞ÄÎä•ÏÑ±)
        localStorage.setItem('auth-tab-hidden-time', Date.now().toString())
      }
    }

    // ÌéòÏù¥ÏßÄ Î°úÎìú Ïãú Ïù¥Ï†Ñ ÏÑ∏ÏÖò ÏÉÅÌÉú ÌôïÏù∏
    const checkPreviousSession = () => {
      const unloadTime = localStorage.getItem('auth-unload-time')
      const tabHiddenTime = localStorage.getItem('auth-tab-hidden-time')

      if (unloadTime || tabHiddenTime) {
        const lastActivity = Math.max(
          parseInt(unloadTime || '0'),
          parseInt(tabHiddenTime || '0')
        )
        const now = Date.now()
        const timeDiff = now - lastActivity

        // 10Î∂Ñ Ïù¥ÏÉÅ ÎπÑÌôúÏÑ± ÏÉÅÌÉúÏòÄÎã§Î©¥ ÏÑ∏ÏÖò Ï¢ÖÎ£å
        if (timeDiff > 10 * 60 * 1000) {
          console.log('Previous session expired due to inactivity')
          authStore.signOut()
        }

        // ÌîåÎûòÍ∑∏ Ï†ïÎ¶¨
        localStorage.removeItem('auth-unload-time')
        localStorage.removeItem('auth-tab-hidden-time')
      }
    }

    // Ïï± ÏãúÏûë Ïãú Ïù¥Ï†Ñ ÏÑ∏ÏÖò ÏÉÅÌÉú ÌôïÏù∏
    checkPreviousSession()

    window.addEventListener('beforeunload', handleBeforeUnload)
    document.addEventListener('visibilitychange', handleVisibilityChange)

    return () => {
      window.removeEventListener('beforeunload', handleBeforeUnload)
      document.removeEventListener('visibilitychange', handleVisibilityChange)
    }
  }, [isClient])

  // Î∏åÎùºÏö∞Ï†Ä ÌÉ≠ Í∞Ñ ÏÑ∏ÏÖò ÎèôÍ∏∞Ìôî - ÌÅ¥ÎùºÏù¥Ïñ∏Ìä∏ÏóêÏÑúÎßå
  useEffect(() => {
    if (!isClient || typeof window === 'undefined') return

    const handleStorageChange = (event: StorageEvent) => {
      // Îã§Î•∏ ÌÉ≠ÏóêÏÑú Î°úÍ∑∏ÏïÑÏõÉÌïú Í≤ΩÏö∞
      if (event.key === 'auth-logout-signal' && event.newValue) {
        console.log('Logout detected in another tab')
        authStore.signOut()
        localStorage.removeItem('auth-logout-signal')
      }
    }

    window.addEventListener('storage', handleStorageChange)

    return () => {
      window.removeEventListener('storage', handleStorageChange)
    }
  }, [isClient])

  const contextValue: AuthContextType = {
    user,
    session,
    profile,
    isLoading,
    isInitializing,
    isAuthenticated,
    isInitialized,
    error,
    signIn: authStore.signIn,
    signUp: authStore.signUp,
    signOut: authStore.signOut,
    resetPassword: authStore.resetPassword,
    updatePassword: authStore.updatePassword,
    refreshSession: authStore.refreshSession,
    validateAndRecoverSession: authStore.validateAndRecoverSession,
    updateProfile: authStore.updateProfile,
    clearError: authStore.clearError,
  }

  return (
    <AuthContext.Provider value={contextValue}>
      {children}
    </AuthContext.Provider>
  )
}

export function useAuth() {
  const context = useContext(AuthContext)
  if (context === undefined) {
    throw new Error('useAuth must be used within an AuthProvider')
  }
  return context
}

// Auth ÏÉÅÌÉúÏóê Îî∞Î•∏ Ï°∞Í±¥Î∂Ä Î†åÎçîÎßÅ Ïª¥Ìè¨ÎÑåÌä∏
interface AuthGuardProps {
  children: ReactNode
  fallback?: ReactNode
  requireAuth?: boolean
}

export function AuthGuard({ children, fallback = null, requireAuth = true }: AuthGuardProps) {
  const { isAuthenticated, isLoading, isInitialized, isInitializing } = useAuth()
  const [isClientReady, setIsClientReady] = useState(false)

  // ÌÅ¥ÎùºÏù¥Ïñ∏Ìä∏ Ï§ÄÎπÑ ÏÉÅÌÉú Í¥ÄÎ¶¨ (SSR Ìò∏ÌôòÏÑ±)
  useEffect(() => {
    setIsClientReady(true)
  }, [])

  // SSRÏóêÏÑúÎäî Î°úÎî© ÏÉÅÌÉúÎ•º ÌëúÏãúÌïòÏßÄ ÏïäÏùå (hydration Î∂àÏùºÏπò Î∞©ÏßÄ)
  if (!isClientReady) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-bg-primary">
        <div className="flex flex-col items-center space-y-4">
          <div className="w-8 h-8 border-2 border-primary-500 border-t-transparent rounded-full animate-spin" />
          <p className="text-text-secondary">ÌéòÏù¥ÏßÄÎ•º Ï§ÄÎπÑÌïòÎäî Ï§ë...</p>
        </div>
      </div>
    )
  }

  // ÌÅ¥ÎùºÏù¥Ïñ∏Ìä∏ÏóêÏÑú Ïù∏Ï¶ùÏù¥ Ï¥àÍ∏∞ÌôîÎêòÏßÄ ÏïäÏïòÍ±∞ÎÇò Î°úÎî© Ï§ëÏù∏ Í≤ΩÏö∞
  if (!isInitialized || isInitializing || isLoading) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-bg-primary">
        <div className="flex flex-col items-center space-y-4">
          <div className="w-8 h-8 border-2 border-primary-500 border-t-transparent rounded-full animate-spin" />
          <p className="text-text-secondary">Ïù∏Ï¶ù ÏÉÅÌÉúÎ•º ÌôïÏù∏ÌïòÎäî Ï§ë...</p>
        </div>
      </div>
    )
  }

  if (requireAuth && !isAuthenticated) {
    return <>{fallback}</>
  }

  if (!requireAuth && isAuthenticated) {
    return <>{fallback}</>
  }

  return <>{children}</>
}
